# In dataset all attributes are 0/1 but only diesease attribute only text
# so that we have to label with label encoder

from sklearn.preprocessing import LabelEncoder

le=LabelEncoder()

df['Disease_encoded']=le.fit_transform(df['Disease'])

x=df.drop(columns=['Disease','Disease_encoded'])
y=df['Disease_encoded']

print(dict(zip(le.classes_ ,le.transform(le.classes_))))

# training and testing the dataset

from sklearn.model_selection import train_test_split

# CELL 8: Train-test split (stratified to keep class balance)
X_train, X_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42, stratify=y
)
print(X_train.shape, X_test.shape)

from sklearn.preprocessing import StandardScaler

continuous_cols = ['Age', 'BP', 'Sugar', 'Oxygen_Level']

scaler=StandardScaler()

x_scale=X_train.copy()
x_test=X_test.copy()

x_scale[continuous_cols]=scaler.fit_transform(X_train[continuous_cols])
x_test[continuous_cols]=scaler.transform(X_test[continuous_cols])

#applying the best model which gives higher accuracy

from sklearn.ensemble import RandomForestClassifier

best_model=RandomForestClassifier(n_estimators=100,random_state=42)
best_model.fit(x_scale,y_train)
y_pred=best_model.predict(x_test)
print("Model Trained!")

# Confusion matrix to predict actual vs predicted values

from sklearn.metrics import confusion_matrix
import matplotlib.pyplot as plt
import seaborn as sns

cm = confusion_matrix(y_test, y_pred)
plt.figure(figsize=(10,8))
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=le.classes_, yticklabels=le.classes_)
plt.xlabel('Predicted'); plt.ylabel('Actual')
plt.title('Confusion Matrix - Random Forest')
plt.xticks(rotation=45, ha='right'); plt.yticks(rotation=0)
plt.tight_layout()
plt.show()

# CELL 15: Feature importance (which symptoms matter most)
